context("Input and output of visualization.t.mixture()")
library("t.mix")

test_that("Inputs are correctly checked", {
  expect_error(visualization.t.mixture())
  expect_error(visualization.t.mixture(NULL))
  expect_error(visualization.t.mixture("a"))
  expect_error(visualization.t.mixture(matrix()))
  expect_error(visualization.t.mixture(matrix(-1)))
  expect_error(visualization.t.mixture(c(-1,0)))
  expect_error(visualization.t.mixture(c(122,0,44,999,0,123,8),NULL))
  expect_error(visualization.t.mixture(c(122,0,44,999,0,123,8),"a"))
  expect_error(visualization.t.mixture(c(122,0,44,999,0,123,8),c(-2,-2)))
  expect_error(visualization.t.mixture(c(122,0,44,999,0,123,8),2))
  expect_error(visualization.t.mixture(c(122,0,44,999,0,123,8), 6:12, NULL))
  expect_error(visualization.t.mixture(c(122,0,44,999,0,123,8), 6:12, "a"))
  expect_error(visualization.t.mixture(c(122,0,44,999,0,123,8), 6:12, -4))
  expect_error(visualization.t.mixture(c(122,0,44,999,0,123,8), 6:12, c(1,2)))
  expect_error(visualization.t.mixture(c(122,0,44,999,0,123,8), 6:12, c(0.4,0.6), NULL))
  expect_error(visualization.t.mixture(c(122,0,44,999,0,123,8), 6:12, c(0.4,0.6), "a"))
  expect_error(visualization.t.mixture(c(122,0,44,999,0,123,8), 6:12, c(0.4,0.6), c(1)))
  expect_error(visualization.t.mixture(c(122,0,44,999,0,123,8), 6:12, c(0.4,0.6), c(1,2), NULL))
  expect_error(visualization.t.mixture(c(122,0,44,999,0,123,8), 6:12, c(0.4,0.6), c(1,2), "a"))
  expect_error(visualization.t.mixture(c(122,0,44,999,0,123,8), 6:12, c(0.4,0.6), c(1,2), c(1)))
  expect_error(visualization.t.mixture(c(122,0,44,999,0,123,8), 6:12, c(0.4,0.6), c(1,2), c(-2,3)))
  expect_error(visualization.t.mixture(c(122,0,44,999,0,123,8), 6:12, c(0.4,0.6), c(1,2), c(4,4)))
  expect_error(visualization.t.mixture(c(122,0,44,999,0,123,8), 6:12, c(0.4,0.6), c(1,2), c(4,4), "a"))
  expect_error(visualization.t.mixture(c(122,0,44,999,0,123,8), 6:12, c(0.4,0.6), c(1,2), c(4,4), c(1)))
  expect_error(visualization.t.mixture(c(122,0,44,999,0,123,8), 6:12, c(0.4,0.6), c(1,2), c(4,4), c(-2,3)))
  expect_error(visualization.t.mixture(c(122,0,44,999,0,123,8), 6:12, c(0.4,0.6), c(1,2), c(4,4), c(10,10), "a"))
  expect_error(visualization.t.mixture(c(122,0,44,999,0,123,8), 6:12, c(0.4,0.6), c(1,2), c(4,4), c(10,10), NULL, "a"))
  expect_error(visualization.t.mixture(c(122,0,44,999,0,123,8), 6:12, c(0.4,0.6), c(1,2), c(4,4), c(10,10), NULL, T, T, T, "a"))
  expect_error(visualization.t.mixture(c(122,0,44,999,0,123,8), 6:12, c(0.4,0.6), c(1,2), c(4,4), c(10,10), NULL, T, T, T, -10))
})


